# ── dispatcher ────────────────────────────────────────────────────────────────

_GENERATORS: Dict[str, Any] = {
    "bulk_rnaseq_analysis": bulk_rnaseq_script,
    "single_cell_analysis": single_cell_script,
    "phylogenetic_tree":    phylogenetic_tree_script,
    "fastqc_quality_analysis": fastqc_script,
}


def generate(
    tool_name: str,
    params: Dict[str, Any],
    *,
    run_id: str,
    session_id: str,
    run_dir: str,
) -> Optional[str]:
    """Return the analysis script for *tool_name*, or None if unsupported."""
    fn = _GENERATORS.get(tool_name)
    if fn is None:
        return None
    safe_params = {k: v for k, v in params.items() if k not in ("needs_inputs",)}
    try:
        return fn(run_id=run_id, session_id=session_id, run_dir=run_dir, **safe_params)
    except TypeError:
        # Unknown extra params — strip to only what the generator accepts
        import inspect
        sig = inspect.signature(fn)
        known = {k for k in sig.parameters if k not in ("run_id", "session_id", "run_dir")}
        filtered = {k: v for k, v in safe_params.items() if k in known}
        return fn(run_id=run_id, session_id=session_id, run_dir=run_dir, **filtered)
