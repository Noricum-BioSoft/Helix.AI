"""
code_generator.py — Deterministic analysis script generator for Helix.AI.

Each public function returns a self-contained Python script (as a string) that:
  • imports only from backend.*
  • embeds all parameters directly (no CLI args needed)
  • writes every output (plots as .png, tables as .json/.csv) to a run_dir/
  • prints a single JSON summary line to stdout when executed

The "Parameters" block near the top of each script is deliberately isolated so
that an LLM can locate and patch it in one focused edit without touching the
analysis logic.
"""

from __future__ import annotations

import textwrap
from pathlib import Path
from typing import Any, Dict, Optional

# Project root is two levels up from this file (backend/ → Helix.AI/)
_PROJECT_ROOT = str(Path(__file__).parent.parent.resolve())


# ── helpers ──────────────────────────────────────────────────────────────────

def _escape(v: Any) -> str:
    """Return a Python-literal representation of v."""
    if isinstance(v, str):
        return repr(v)
    return repr(v)


# ── bulk RNA-seq ──────────────────────────────────────────────────────────────

_BULK_RNASEQ_TEMPLATE = '''\
#!/usr/bin/env python3
"""
Bulk RNA-seq Differential Expression Analysis
Generated by Helix.AI  |  run_id: {run_id}  |  session: {session_id}
"""
import sys, json, base64
from pathlib import Path

# ── Parameters ────────────────────────────────────────────────────────────────
# Edit this block to change the analysis and re-run the script.
COUNT_MATRIX     = {count_matrix}
SAMPLE_METADATA  = {sample_metadata}
DESIGN_FORMULA   = {design_formula}
ALPHA            = {alpha}
X_SCALE          = {x_scale}   # "log2" or "linear"
RUN_DIR          = Path({run_dir})
# ─────────────────────────────────────────────────────────────────────────────

sys.path.insert(0, {project_root})
from backend import bulk_rnaseq as _mod

RUN_DIR.mkdir(parents=True, exist_ok=True)
plots_dir  = RUN_DIR / "plots";  plots_dir.mkdir(exist_ok=True)
tables_dir = RUN_DIR / "tables"; tables_dir.mkdir(exist_ok=True)

result = _mod.run_deseq2_analysis(
    count_matrix=COUNT_MATRIX,
    sample_metadata=SAMPLE_METADATA,
    design_formula=DESIGN_FORMULA,
    alpha=ALPHA,
    x_scale=X_SCALE,
)

# ── Save plots ────────────────────────────────────────────────────────────────
plot_paths = {{}}
for key, b64 in (result.get("plots") or {{}}).items():
    if not b64:
        continue
    out = plots_dir / f"{{key}}.png"
    out.write_bytes(base64.b64decode(b64))
    plot_paths[key] = str(out)

# ── Save DE tables ────────────────────────────────────────────────────────────
table_paths = {{}}
for key, records in (result.get("de_results") or {{}}).items():
    if not records:
        continue
    out = tables_dir / f"de_{{key}}.json"
    out.write_text(json.dumps(records, indent=2))
    table_paths[key] = str(out)

# ── Summary ───────────────────────────────────────────────────────────────────
summary = {{
    "status":       result.get("status", "success"),
    "n_samples":    result.get("n_samples"),
    "n_genes_total": result.get("n_genes_total"),
    "summary":      result.get("summary"),
    "top_genes":    result.get("top_genes"),
    "plot_paths":   plot_paths,
    "table_paths":  table_paths,
    "x_scale":      X_SCALE,
    "run_dir":      str(RUN_DIR),
}}
print(json.dumps(summary))
'''


def bulk_rnaseq_script(
    *,
    run_id: str,
    session_id: str,
    run_dir: str,
    count_matrix: str,
    sample_metadata: str,
    design_formula: str = "~condition",
    alpha: float = 0.05,
    x_scale: str = "log2",
) -> str:
    return _BULK_RNASEQ_TEMPLATE.format(
        run_id=run_id,
        session_id=session_id,
        count_matrix=_escape(count_matrix),
        sample_metadata=_escape(sample_metadata),
        design_formula=_escape(design_formula),
        alpha=_escape(alpha),
        x_scale=_escape(x_scale),
        run_dir=_escape(run_dir),
        project_root=_escape(_PROJECT_ROOT),
    )


